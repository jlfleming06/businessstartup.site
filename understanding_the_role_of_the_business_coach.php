			<P>The business coach is an important asset that savvy businesses and organizations are taking advantage of their services. Still, many don’t fully understand the role of the business coach in today’s modern business, so let’s have a look.<br>
<br>
#1 A Business Coach Supports the Employee<br>
<br>
It’s usually executives or managers that seek guidance or input from a coach when they are not sure how to handle a certain situation. Sometimes help is sought before a tentative or difficult situation is dealt with. Managers will often seek assistance with their own growth as a manager, which can leave coaches with a very difficult and delicate situation. <br>
<br>
A coach has the ability to improve the executive’s abilities and self esteem, especially if there is confirmation that his or her answer is right. If a coach doesn’t know what the right answer or is speculating the correct course of action is the truth. It’s far better to say you do not know than to give bad advice. <br>
<br>
#2 A Business Coach Helps Manager to Develop Their Solutions<br>
<br>
People usually know how to act or what they should do. Many time’s the job of the coach is to draw out the answer from the person, because if you give him/her the answer then they are less likely to own the solution. A coach can offer options and make resource recommendations, even give opinions. A coach can answer questions, but in the end the manager must have the answer.  <br>
<br>
#3 A Coach Must Have Communication Skills That Are Sharpened<br>
<br>
Listening is a key skill all coaches need to have in order to be able to understand the actual needs of the manager who is asking for their assistance. It is a coach weakness when he or she assumes the current situation or question is like one previously encountered. The coach needs to be fully engaged and aware to take in the information that is being provided. Open ended questions are necessary to draw out the manager. Listening includes watching body language, facial expression, tone of voice, and movements. <br>
<br>
#4 A Coach is a Teacher or Educator<br>
<br>
As an external coach you teach managers and leaders as you work with them. The goal is to make them self sufficient. The role of the coach is to provide them with the tools they need to be successful in their business arena.</P>
