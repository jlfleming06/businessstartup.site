			<P>Business coaching – what is it? Executives and managers of companies who are interested in development and career growth increasingly turn to a business coach to have a customized development process. Consider this – how often would you have found it helpful to have a chance to talk about some key ideas with a person that is impartial and objective in preparation for a major change or perhaps a very important meeting. <br>
<br>
The concept of business coaching has actually come from the sports arena where top athletes employ coaches to help improve and enhance their performance. Business people are slowly realizing that the same benefits can be enjoyed in the business arena using business coaches. <br>
<br>
Coaching works best in those environments where there is a desire to be practical, progressive and proactive but at the same time there is value in the opportunity to challenge and stimulate. The executive(s) or manager(s) is thought to be the expert in his/her field and in this way the business coach will facilitated the executive or manager to find the right way forward. Coaching is always forward thinking with a well-structured approach that remains flexible and presumes purpose and commitment. <br>
<br>
Effective, Efficient, Productive<br>
<br>
When it comes to professional support, business coaching is the exception as it combines individual attention, challenge and objectiveness. The coach’s skills make it extremely successful because:<br>
<br>
It inspires you to develop your own solutions<br>
It keeps you focusing on a specific goal or goals<br>
It makes you accountable for your progress<br>
It supports you through change<br>
It removes any blockages so that you can move forward<br>
It prioritizes based on values<br>
It challenges you to take the next step in moving forward<br>
<br>
Research published in Olivero et al. 1997 showed that training can increase productivity by as much as 22 percent, but when training is combined with coaching, it can increase productivity by a whopping 88 percent. <br>
<br>
What Does Business Coaching Involve?<br>
<br>
Coaching is not training – the delivery system is much different. A business coach will work with the executive or manager to develop a training program that is tailored to the skill areas where there is a need for impact. The coach will help the executive/manager to make the necessary behavioral changes to create growth. The coach does not provide the answer, but rather the coach brings along a process or system to help the client determine the answers.</P>
