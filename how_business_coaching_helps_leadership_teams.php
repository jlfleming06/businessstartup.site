			<P>Business coaching is an important tool that can offer a unique advantage if you want to coach your leadership teams, especially in a growing business. Business coaching can help to create an environment that is healthy for leadership growth that can play a key role as the business also grows. <br>
<br>
It can help leaders to set strategic direction, develop marketing strategies and operating tactics in a low risk and non stressful manner that will improve the numbers and create a business that can run without being dependent on the owner or CEO. <br>
<br>
The training manual used by the business coach should walk through the needs of the company. The business coach should also ensure that the members of the leadership team have their needs explored, their skills analyzed and their goals discussed. In that way, it’s easier to make sure your leadership team members are strategically placed to maximize the benefits to the company and create the best environment for the leaders. <br>
<br>
You should not confuse coaching with training, as they are different, especially when it comes to the delivery system. A coach will work with management to tailor a training program in the skill areas where there is a need for impact. The coach will also help managers to make behavioral changes that are seen as necessary for growth. The significance is that the coach and the manager(s) must be clear on the competencies that will have an effect on the bottom line. It is essential that the competencies are measured before and after coaching so that there can be an evaluation of the effectiveness.<br>
<br>
At the beginning the relationship is determined – is the coach considered a trusted friend or an advisor? Does the coach listen and provide feedback or does the coach help the manager obtain 360-degree feedback and then develop an action plan to increase performance? The role and the relationship need to be established by both parties before the coaching project begins. <br>
<br>
The coach will push the window with each manager to help them grow professionally and to promote the company’s success and the success of the leadership team. A good way to do this is to create a situation so that each leadership team member will ask for help from the coach, rather than the coach forcing their help on the manager. <br>
<br>
Business coaching doesn’t look just one way. It is designed to be created to work specific to the needs of the client and in doing so the delivery can be tailor made.</P>
