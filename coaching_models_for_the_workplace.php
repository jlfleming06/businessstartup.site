			<P>The earliest coaching models date back the 1950s and through the 1980s, they were still static models. It wasn’t until the 1990s, when sports coaching models began to grow in popularity. A coaching model needs to be flexible and adaptable so that it can be tailored to the specific needs of the client. Today’s coaching model should:<br>
<br>
* Presents a complete, detailed description of the process that’s involved<br>
* It should build towards an outcome that’s predicted<br>
* It should establish the nature of the components<br>
* It uses valid methodology to produce change that is sustainable and measurable<br>
<br>
The traditional coaching model was built around change models that were outdated like the grow model, action planning, goal setting, etc. fall very short of creating lasting behavior competencies that are measurable. Using a ‘one size fits all’ approach is simplistic and ineffective because it ignores the person’s behavioral pattern that controls how the skill that’s going to be improved is executed. <br>
<br>
With traditional coaching very shortly after those who participated revert back to their old behaviors after the event. It seems it’s a pretty expensive venture for a quick buzz that fades fast. The challenge for development professionals is to improve the organization's effectiveness through behavioral change that involves a learning model that strengthens the individuals and sustains the behavioral skills. <br>
<br>
The 21st century professional coaching is a combined approach that the behavioral sciences founded. Today’s coaching model includes personal beliefs, development, attitudes, values, motivation, social learning and emotions along with organizational and personal dynamics. <br>
<br>
Many mechanisms of the behavioral based coaching model come from the behavioral approach to learning and changing. Some of the coaching model aspects are:<br>
<br>
* Targeting and then focusing on an explicit behavior <br>
* Analyzing a behavior in relation to its precursor and the consequences<br>
* Applying a reliable and valid method of data collection, data analysis and assessment<br>
* Building of a developmental plan<br>
* Employing behavioral change techniques that are validated<br>
* Measuring, managing and maintaining behavioral change<br>
<br>
Coaching models for the workplace need to be effective long term. They shouldn’t provide the answers, but rather the circumstances for the participant(s) to come up with their own solutions that will work. The coach is a bit like a sounding board opening up a dialogue that will help the participants to grow and prosper and that can help a business to grow and prosper as well.</P>
