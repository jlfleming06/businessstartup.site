			<P>The one on one coaching model is an excellent choice if you want to invest in the talent for the future. It can play a positive role in the long-term strategy and yet often it is a strategy that’s overlooked. The coaching relationship is going to enhance the skills that are needed during difficult and changing times. When your company invests in this type of development of company talent, they are looking at a long-term strategy for their company and one that can be created using the one on one coaching model. It looks similar to this.<br>
<br>
* The coach will meet with the manager(s) and Human Resource Dept. to talk about the goals of the candidate and they will come to an agreement on the targets.<br>
<br>
* The coach will meet with the candidate to collect personal history, career data and then to reach an agreement on the goals and expectations.<br>
<br>
* The assessment process will begin by using a variety of instruments along with the 360 feedback from employees that work in close proximity with the candidate.<br>
<br>
* Feedback is collected and then it is analyzed to decide on assets and liabilities, which are then presented to the candidate.<br>
<br>
* The candidate will create a developmental plan that’s specific to behavior and determine the desired outcome that will also include input from the manager and/or Human Resources personal.<br>
<br>
* The plan is implemented so that it also has time frames and suitable tools.<br>
<br>
* The coach works with the candidate to incorporate the new behaviors with the regular one-on-one sessions.<br>
<br>
* The coach will reassess the close-working employees, and then evaluate and report the incremental shifts. Engagement will extend when there is revised objectives.<br>
<br>
Choosing a Coach<br>
<br>
Choosing the right coach is important. It means you need to find out who best fits the candidate. Personalities vary, but still professionals should be able to blend with a mix of personalities. A professional coach should have these qualifications:<br>
<br>
* Experience in the industry.<br>
* Exposure to working with senior staff and the types of issues they face.<br>
* Exposure to and/or certification in numerous assessment tools.<br>
* Being able to provide feedback.<br>
* Five or more years of corporate coaching are preferred.<br>
* Expertise and significant exposure in organizational changes.<br>
<br>
You’ve heard it before – when the going gets tough the tough get going, or they should take advantage of one on one coaching to help create opportunities and make goals. A coach is able to reinforce the needed skills for both current personal and new personnel so that they can move forward.</P>
