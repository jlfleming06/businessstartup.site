			<P>Many people are unaware that there are a number of different coaching styles that can be used by a coach. Let’s have a look at some of these different coaching styles.<br>
<br>
* Traditional Coaching Model — The coach identifies with how the client is feeling. The coach acts as a guide and confidant.<br>
<br>
* Advanced Coaching Model — Useful when there is a need to make a considerable break from the past, and where the person wants to try new things or a new way of doing something.<br>
<br>
* Block Removal Coaching — This coaching model works when a person is resistant to growth, which causes a block, that usually the result of a hidden fear or limiting beliefs. <br>
<br>
* Innovation Coaching Model — This coaching model focuses on experimentation, creativity and innovation.<br>
<br>
* 3-D Coaching Model — This coaching model works on who a person is and what they want, then how to get it.<br>
<br>
* Personal Foundation Model — The coach will work on standards and boundaries. The higher a person wants to go, the deeper the foundation has to be.<br>
<br>
* Bigger Thinking Model — The coach will challenge the person’s assumptions and work to break one out of their comfort zone and expand their thinking!<br>
<br>
* Solution Coaching — The coach will identify symptoms, find out the source of those symptoms, and then work with a person to find a solution.<br>
<br>
* Shift Coaching Model — When a person isn’t able to determine how to achieve their goals, a shift in thinking can help. The coach can help a person to step back, shift, and then move forward.<br>
<br>
* Deep Coaching — There can be a shift in basic beliefs over time until these beliefs simply are not true any longer. The coach can help a person dig deep to discover the 'out of date truth' which a person can then align with the reality of today.<br>
<br>
* Performance Coaching Model — For the client that is competitive, the coach can help by developing a persuasive goal, creating milestones to help you focus on your goals, and building momentum through reporting daily.<br>
<br>
* Acceptance Coaching Model — Rather than a person trying to overcome a weakness, the coach can help to accept it and use it to their advantage. <br>
<br>
* Intermediate Model — A good model when a person wants the coach to work in partnership on developing goals and then on achieving them.<br>
<br>
As you can see, there are many different ways that business coaching can be approached. It can pay to match the coach’s style with your needs to get the best results. Talk to the coach you are considering hiring and see what style they use. Don’t be afraid to ask questions so you make the right choice.</P>
