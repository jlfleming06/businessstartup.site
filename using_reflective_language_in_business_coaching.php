			<P>Business coaching is a powerful tool, and it becomes even more useful when reflective language is used, which is the reflecting back of what the coach says in his or her own words. This leads to a win-win situation, ideal for success. <br>
<br>
With reflective language, the coach isn’t using mental activity to interpret and there is no need for the coach to become detached from their experience because they are worried about the meaning of the coach’s words. The best coaches use reflective language and while the coaches might not notice when reflective language is used, those observing most certainly do.  <br>
<br>
The reflective language is intended to help the employees stay connected to their discovery process. The words expressed are then reflected back in as perfect a form as possible so that there is no need to translate them. <br>
<br>
Words are let out and then reflected right back, without any resistance. The coach never once questions the words because the words are theirs. The process of reflective questioning is so perfect that the employee doesn’t even notice the sentence construction and they are able to stay focused on their own thinking and discovery evolution. <br>
<br>
Sub Modalities<br>
<br>
Sub modalities a just experience variants. In coaching, there is the possibility of extending the employee’s experiences in the world by inviting the audience to focus on their experiences and then play them. This isn’t a new concept dating back to the 1960s, but it still remains important today. When you explore the sub modalities of experiences it can lead to a change in visual and auditory representation. <br>
<br>
When you watch a person’s language, you will get clues on how they store information. Inviting an employee to experiment with sub modalities of their experience can help them to find ways that are more constructive to manage situations in the future and it can provide a routine support system for those tough scenarios. <br>
<br>
Sub modalities can be a bit hard to understand, but there is a great deal of information available online that will help you to better understand. You will even be able to find some working examples. <br>
<br>
Reflective language is a powerful tool that business coaches can use when working with employees. It’s a tool that is often overlooked and yet has so much value. Business coaching is a powerful tool that most companies can really benefit from.</P>
