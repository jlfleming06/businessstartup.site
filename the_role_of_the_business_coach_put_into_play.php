			<P>The concept of business coaching actually got its start on the sports field. In sports, top athletes have coaches to help improve their performance and enhance their abilities. That concept has been transposed to the players in the business arena who have realized that the same methodology can apply in the business environment. Executives and managers can improve their performance and enhance their skills by having a business coach.<br>
<br>
Initially a meeting needs to be established with the business coach who will assist in identifying the requirements so that a specific program can be created to meet those requirements. Coaching can also be extended out to employee groups or kept at the individual level. The business coach isn’t there to bring the answers. Their role is to bring a process or a system that will help the client to discover their own answers. <br>
<br>
Accepted as a Coach<br>
<br>
An effective coach will define the relationship right at the beginning. The role could be that of a trusted advisor or friend, it could as a sounding board and to provide feedback, it could be as a performer to help create a plan. This needs to be established early and this role must accepted by both parties if it’s going to work. <br>
<br>
The coach will push the envelope with each manager he/she works with so that they can grow professionally and play a major role in both the company’s success and their individual success.   <br>
<br>
The Coach Isn’t the One in Control<br>
<br>
It is important to remember that the coach is a resource and he/she is not in control of the relationship, the decisions or the actions of the person being coached. Sometimes the coach does form a partnership with the coached manager that helps develop good decisions and choices for the company and the manager’s personal growth. However, ultimately the manager will make the final decisions, not the coach. Your effectiveness as a communicator and your knowledge along with the relationship you have developed will ultimately play a role in whether a manager chooses to incorporate any recommendations you might have. <br>
<br>
The role of the coach should be clear. As important as the coach can be to a company and the management and staff, they are the outsider looking in and it is very important that clear and concise guidelines are established from the very beginning to make sure that no lines are crossed.</P>
