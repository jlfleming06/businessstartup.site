			<P>Those companies that are highly effective take great pains to make sure that they have established a clear vision for their company along with a clear and concise strategy of how they want to implement that vision. They carefully recruit and retain those employees who are the best to help them carry out their strategy. After all, it is common sense that if you want your company to have a high degree of success you need to have high and continuous employee production, because you can’t do it alone. This means you also have continually motivate employees. <br>
<br>
By definition, employee motivation is a key component of the employee productivity equation. It is in this area where you will find that business coaching can have the biggest impact. You can temporarily boost your employees’ motivation using incentives, especially financial incentives, but these are not sustainable employee motivation methods and they will stop working. Business coaching can help you to create sustainable employee motivation. <br>
<br>
A company establishes its vision, mission and execution strategy. An employee also needs to establish their own vision. It will just be minus all the corporate ‘talk,’ and it will be their terms, what they want out of their career at the company.<br>
<br>
A business coach will support employees using a mix of individual sessions and group sessions. They will help employees to articulate what it is they want, their goals, and then help them to determine how they might reach those goals in relation to the company they work for.<br>
<br>
Employees usually find these types of sessions far more useful than when companies use ‘Personal Development Plan’ templates. Actually, when the results are structured right, they can actually take the templates and transfer them to work effectively for personal development. <br>
<br>
Facilitative coaching sessions will take a bottom up data approach that details the skills of each employee and what their motivators are to reconcile with the objectives of the business. The focus is to align corporate objectives to the needs of the employees. <br>
<br>
Of course, not all employees will get what it is they want. It will be up to your management team to negotiate a solution that is a win-win for everyone involved. Management will also have to effectively create the basis for the sustainable employee motivation to get the most out of the process.</P>
